                        <h2 class="uk-heading-bullet">What is Door Knock?</h2>
                        <p>
                            Door Knock is an Online Grocery Order and Delivery System. Why is Door Knock? Why you cannot do a Grocery Order and Delivery by an ecommerce theme.
                        </p>
                        <p>
                            The simple answer is “User Experience and Users Expectation are not same”when the user visits for buying a shirt and order for his grocery needs for home.
                        </p>
                        <ol>
                            <li>
                                When he goes to order a shirt he will go to multiple sites to see which one has best design, but for grocery he will not have time to visit for his 15 items.
                            </li>
                            <li>
                                He will visit almost all the shirts is available in the site, expects to see a big image of the shirt, but for grocery he does not need to see a big image of the potato packet and he know which potato he buys always, he will select that he even will not have option to choose from more than 4/5 type potato. 
                            </li>
                            <li>
                                He will not want to leave the page to sign up after adding all items to cart.
                            </li>
                            <li>
                                He will not wait for 3 seconds to search another item after searching one item.
                            </li>
                        </ol>
                        <p>
                            So the main thing is, customer wants fast user experience so that he can place multiple items very quickly.
                        </p>
                        <p>
                            And second thing is, you cannot run your business smoothly by managing multiple things is different platforms. Like taking order and managing delivery from a website and keeping other things like purchase, expense, salary, stock in another system.
                        </p>
